void delayMS(uint32 ms){
    uint32 stmFreq = IfxScuCcu_getStmFrequency();
    uint64 ticks = ((uint64)stmFreq * ms) / 1000;
    uint32 startTime = IfxStm_getLower(&MODULE_STM0);
    while((IfxStm_getLower(&MODULE_STM0) - startTime) < ticks);
}

